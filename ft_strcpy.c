#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int	i;
	int j;

	i = 0;
	j = 0;
	while (src[i] != '\0')
	{
		dst[j] = src[i];
		i++;
		j++;
	}
	dst[j] = '\0';
	return (dst);
}
