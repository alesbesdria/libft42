#include <libft.h>

void	ft_putendl_fd(char const *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i]);
		i++;
	}
	ft_putchar_fd('\n');
	return (0);
}
