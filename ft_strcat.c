#include <libft.h>

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s1[i] != '\0' && s2[j] != '\0')
	{
		i++;
		j++;
	}
	return (s1[i] - 1, s2[j]);
}
